
public class number48{
	public static void main(String args[])
	{
		int sp=5;
		int z=1;
		for(int i=1;i<=5;i++)
		{
			for(int j=1;j<=sp;j++)
			{
				System.out.print(" ");
			
			}
			sp--;
			for(int k=1;k<=z;k++)
			{
			System.out.print(k+" ");
			}
			z++;
			System.out.println(" ");
		}
		
}
}
