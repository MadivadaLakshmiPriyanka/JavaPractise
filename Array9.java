
public class Array9{
	public static void main(String args[])
	{
		
		char[] a={'H','a','r','i','s','h','c','h','a','n','d','r','a'};	
		
		for(int i=0;i<a.length;i++)
		{
			if(a[i]=='a'||a[i]=='e'||a[i]=='i'||a[i]=='o'||a[i]=='u')
			{
				System.out.println(a[i]);
			}
		}
				
			
			
		
}
}
